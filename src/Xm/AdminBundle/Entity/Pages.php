<?php

namespace Xm\AdminBundle\Entity;

use Doctrine\ORM\Mapping as ORM;
/**
 * Pages
 *
 * @ORM\Table(name="pages")
 * @ORM\Entity
 */
class Pages
{
    /**
     * @var integer
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="IDENTITY")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="flag", type="string", length=45, nullable=true)
     */
    private $flag;

    /**
     * @var string
     *
     * @ORM\Column(name="content", type="text", nullable=true)
     */
    private $content;

    /**
     * @var \DateTime
     *
     * @ORM\Column(name="last_modify", type="datetime", nullable=true)
     */
    private $lastModify;

    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set flag
     *
     * @param string $flag
     *
     * @return Pages
     */
    public function setFlag($flag)
    {
        $this->flag = $flag;

        return $this;
    }

    /**
     * Get flag
     *
     * @return string
     */
    public function getFlag()
    {
        return $this->flag;
    }

    /**
     * Set content
     *
     * @param string $content
     *
     * @return Pages
     */
    public function setContent($content)
    {
        $this->content = $content;

        return $this;
    }

    /**
     * Get content
     *
     * @return string
     */
    public function getContent()
    {
        return $this->content;
    }

    /**
     * Set lastModify
     *
     * @param Datetime $lastModify
     *
     * @return Pages
     */
    public function setLastModify($lastModify)
    {
        $this->lastModify = $lastModify;

        return $this;
    }

    /**
     * Get lastModify
     *
     * @return Datetime
     */
    public function getLastModify()
    {
        return $this->lastModify;
    }
}
